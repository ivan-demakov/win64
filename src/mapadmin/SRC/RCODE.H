#ifndef _RCODE_H_
#define _RCODE_H_
//=====================================================================
enum ResultCode         // коды результата файловых операций
{
  R_OK              =   0, // успешное завершение
  R_REPEAT          =   1, // успешное неполное завершение
  R_BADFILESTRUCT   =   2, // ошибка - некорректная структура данных
  R_OUTOFMEMORY     =   3, // ошибка - невозможно выделить память
  R_BADSTREAM       =   4, // ошибка - некорректный поток
  R_EOFSTREAM       =   5, // ошибка - конец файла
  R_FAILSTREAM      =   6, // ошибка - сбой файловой операции
  R_NOFILE          =   7, // ошибка - файл не найден
  R_NOPROJECT       =   8, // ошибка - нет определения пути проекта
  R_NOIDENT         =   9, // ошибка - нет объекта с указанным идентификатором
  R_CONVERTERROR    =  10, // ошибка - недопустимые данные для конвертации
  R_UNKNOWNERROR    =  11, // ошибка - неизвестная ошибка
  R_FLUSHERROR      =  12, // ошибка - flush
  R_MAPNOTOPEN      =  13, // ошибка - map not open
  R_DUPIDENT        =  14, // ошибка - уже есть объект с указанным идентификатором
  R_BADRECORDHEAD   =  15, // ошибка - некорректный заголовок записи
  R_INFORMATION     =  16, // информация
  R_LOGUSER         =  17, // вход
  R_STOPUSER        =  18, // выход
  R_HUDEOBJECT      =  19, // слишком большой объект
  R_BADOBJECT       =  20, // некорректный объект
  R_BACKUPERROR     =  21, // ошибка - backup
  R_COMERROR        = 100,
  R_METODERR        = 101,
  R_CLI_NOINIT      = 102,
  R_SAFEARR_CRE_ERR = 103,
  R_SAFEARR_ACC_ERR = 104,
  R_PARAM_ERR       = 105,
  R_SAFEARR_SEM_ERR = 106,
  R_IDENT_ERR       = 107
};
//=====================================================================
#endif
